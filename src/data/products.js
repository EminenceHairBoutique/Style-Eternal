  products.filter((p) => p.collectionSlug === collectionSlug);

export const getProductsByCategory = (category) =>
  products.filter((p) => p.category === category);

export const getProductsByDrop = (drop) =>
  products.filter((p) => p.drop === drop);

export const getAvailableProducts = () =>
  products.filter((p) => p.releaseStatus !== "sold-out");

export const getNewArrivals = () =>
  products.filter((p) => p.isNew && p.releaseStatus === "available");

